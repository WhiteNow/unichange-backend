package hackathon.cinqchainz;

public class Convenio {

    public int id;
    public String universidad;
    public String pais;
    public String estado;

    public Convenio(int id, String universidad, String pais, String estado) {
        this.id = id;
        this.universidad = universidad;
        this.pais = pais;
        this.estado = estado;
    }

}
